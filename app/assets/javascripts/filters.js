'use strict';

/* Filters */
var rubyFilter = angular.module('ruby.filters', []);

rubyFilter
    .filter('capitalize', function() {
        return function(input) {
            return input ? input[0].toUpperCase() + input.slice(1).toLowerCase() : '';
        };
    })
    .filter('characters', function () {
        return function (input, chars, breakOnWord) {
            if (isNaN(chars)) return input;
            if (chars <= 0) return '';
            if (input && input.length > chars) {
                input = input.substring(0, chars);

                if (!breakOnWord) {
                    var lastspace = input.lastIndexOf(' ');
                    //get last space
                    if (lastspace !== -1) {
                        input = input.substr(0, lastspace);
                    }
                }else{
                    while(input.charAt(input.length-1) === ' '){
                        input = input.substr(0, input.length -1);
                    }
                }
                return input + '...';
            }
            return input;
        };
    })
    .filter('words', function () {
        return function (input, words) {
            if (isNaN(words)) return input;
            if (words <= 0) return '';
            if (input) {
                var inputWords = input.split(/\s+/);
                if (inputWords.length > words) {
                    input = inputWords.slice(0, words).join(' ') + '...';
                }
            }
            return input;
        };
    })
    .filter('logsLevel', function() {
        return function(logs, level) {
            return logs.filter(function(log) {
                return level[log.level] !== undefined && level[log.level] === true;
            });
        }
    });